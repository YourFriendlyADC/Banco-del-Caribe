    // Recorre el archivo
    public static boolean Leer(String fileName, String id) {
        boolean hay = false;
        while (hay == false) {
            try {
                BufferedReader br = new BufferedReader(new FileReader(fileName + ".txt"));
                String line = null; //definición de line
                while ((line = br.readLine()) != null) {
                    String temp[] = line.split(":");
                    if (id.equals(temp[0])) {
                        return true;
                    }
                }
                br.close();
                hay = true;
            } catch (IOException ex) {
                System.out.println("No se encontro archivo");
                hay = false;
            }
        }
        return false;
    }

    public static void rehacer(String fileTName, String id, String fileTNameNew) {
        boolean hay = false;
        // nuevo archivo
        FileWriter fileWriter = null;
        PrintWriter lineWriter = null;
        File archiveNameNew = new File(fileTNameNew + ".txt");

        try {
            if (!archiveNameNew.exists()) {
                archiveNameNew.createNewFile();
            }
            fileWriter = new FileWriter(archiveNameNew, true);
            lineWriter = new PrintWriter(fileWriter);
        } catch (IOException ex) {
            //ErrorD.setText("Error creando el archivo");
            ex.printStackTrace();
        }

        boolean isTrans = false;
        while (hay == false) {
            try {
                BufferedReader br2 = new BufferedReader(new FileReader(fileTName + ".txt"));
                String line = null; //definición de line
                while ((line = br2.readLine()) != null) {
                    String temp[] = line.split(":");
                    System.out.println("isTrans " + isTrans);
                    if (id.equals(temp[0]) && isTrans == false) {
                        isTrans = true;
                    } else if (!id.equals(temp[0])) {
                        lineWriter.println(temp[0] + ":" + temp[1] + ":" + temp[2]);
                    }
                }
                br2.close();
                hay = true;
                lineWriter.close();
                fileWriter.close();
            } catch (IOException ex) {
                System.out.println("No se encontro archivo");
                hay = false;
            }
        }
    }
