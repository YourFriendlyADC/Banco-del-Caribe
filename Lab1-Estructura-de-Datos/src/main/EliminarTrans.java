    private void insertPanelMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertPanelMousePressed
        if (transToDelete.getText().isEmpty()) {
            ErrorF.setText("Digite todos los campos");
            confirmRegistroText.setForeground(new java.awt.Color(159, 101, 100));
            confirmRegistroText.setText("La transacción no se ha eliminado");
            confirmRegistroIcon.setIcon(new ImageIcon(new javax.swing.ImageIcon(getClass().getResource("/images/wrong.png")).getImage().getScaledInstance(240, 240, Image.SCALE_SMOOTH)));
        } else {
            ErrorF.setText("");
            String idTFieldToDelete = transToDelete.getText();
            String fileTName = "Transacciones";
            String fileTNameNew = fileTName + "New";
            File archiveTName = new File(fileTName + ".txt");
            File archiveTNameNew = new File(fileTNameNew + ".txt");
            PrintWriter lineWriter;
            try {
                if (!archiveTName.exists()) {
                    archiveTName.createNewFile();
                }

                // Cedula
                if (idTFieldToDelete.length() < 1) {
                    transToDelete.setText("Cédula incorrecta");
                    ErrorF.setText("Digite la cédula correctamente");
                }
                // Verifica si está
                if (Leer(fileTName, idTFieldToDelete)) {
                    confirmRegistroText.setForeground(new java.awt.Color(96, 107, 167));
                    confirmRegistroText.setText("La transacción se ha eliminado correctamente");
                    confirmRegistroIcon.setIcon(new ImageIcon(new javax.swing.ImageIcon(getClass().getResource("/images/check.png")).getImage().getScaledInstance(240, 240, Image.SCALE_SMOOTH)));
                    rehacer(fileTName, idTFieldToDelete, fileTNameNew);
                    archiveTName.delete();
                    lineWriter = null;
                    File NewFile = new File("Transacciones.txt");
                    lineWriter = new PrintWriter(NewFile);
                    try {
                        BufferedReader bro = new BufferedReader(new FileReader(fileTNameNew + ".txt"));
                        String line = null; //definición de line
                        while ((line = bro.readLine()) != null) {
                            String temp[] = line.split(":");
                            lineWriter.println(temp[0] + ":" + temp[1] + ":" + temp[2]);
                        }
                        bro.close();
                        lineWriter.close();
                        archiveTNameNew.delete();
                    } catch (IOException ex) {
                        System.out.println("No se encontro archivo");
                    }
                } else {
                    ErrorF.setText("La cédula no está registrada");
                    confirmRegistroText.setForeground(new java.awt.Color(159, 101, 100));
                    confirmRegistroText.setText("La transacción no se ha eliminado");
                    confirmRegistroIcon.setIcon(new ImageIcon(new javax.swing.ImageIcon(getClass().getResource("/images/wrong.png")).getImage().getScaledInstance(240, 240, Image.SCALE_SMOOTH)));
                }
            } catch (IOException ex) {
                ErrorF.setText("Error creando el archivo");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_insertPanelMousePressed

